        <h1 class="title-font">Overweight and obesity</h1>
        <div>
            <em>Overweight and obesity</em> are defined as abnormal or excessive fat accumulation that
            presents a risk to health. A crude population measure of obesity is the body mass
            index (BMI), a person’s weight (in kilograms) divided by the square of his or her
            height (in metres). A person with a BMI of 30 or more is generally considered obese.
            A person with a BMI equal to or more than 25 is considered overweight. Overweight
            and obesity are major risk factors for a number of chronic diseases, including
            diabetes, cardiovascular diseases and cancer. Once considered a problem only in
            high income countries, overweight and obesity are now dramatically on the rise
            in low- and middle-income countries, particularly in urban settings.
        </div>
