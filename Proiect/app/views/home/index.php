    <h1 class="title-font">Behavioral Risk Factor Surveillance System</h1>
    <div>
        The <em>Behavioral Risk Factor Surveillance System (BRFSS)</em> is the nation’s premier system of
        health-related telephone surveys that collect state data about U.S. residents regarding
        their health-related risk behaviors, chronic health conditions, and use of preventive
        services. Established in 1984 with 15 states, BRFSS now collects data in all 50 states as
        well as the District of Columbia and three U.S. territories. BRFSS completes more than 400,000
        adult interviews each year, making it the largest continuously conducted health survey system in the world.
    </div>
